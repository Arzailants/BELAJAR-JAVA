package belajar3_karakter_escape;

/**
 *
 * @author vaio
 */
public class BELAJAR3_KARAKTER_ESCAPE {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
    // KARAKTER ESCAPE \n
    System.out.println("\n = Pindah Baris / Linefeed");
    System.out.println("ALICE SYNTHESIS THIRTY \n");
    
    
    // KARAKTER ESCAPE \t
    System.out.println("\t = Tab Horizontal");
    System.out.println("ALICE SYNTHESIS THIRTY \t \n");
    
    
    // KARAKTER ESCAPE \r
    System.out.println("\r = Carriage Return");
    System.out.println("ALICE SYNTHESIS THIRTY \r \n");
    
    
    // KARAKTER ESCAPE \f
    System.out.println("\f = Formfeed");
    System.out.println("ALICE SYNTHESIS THIRTY \f \n");
    
    
      // KARAKTER ESCAPE \b
    System.out.println("\b = Bel");
    System.out.println("ALICE SYNTHESIS THIRTY \b \n");
    
    
      // KARAKTER ESCAPE \\
    System.out.println("\\ = Pemberian Karakter");
    System.out.println("ALICE SYNTHESIS THIRTY \\");
    }
    
}
